                     <p>Theo nghĩa Hán &#8211; Việt, &#8220;Cát&#8221; (Hay còn gọi là &#8220;Kiết&#8221;) có nghĩa là
                        tốt lành, &#8220;Tường&#8221; có nghĩa là điều may mắn, phước lành. &#8220;Cát Tường&#8221; dùng
                        để nói đến những điều tốt lành, sự viên mãn, hạnh phúc. Vì vậy mà trưng hoa Cát Tường trong nhà
                        vào đầu năm mới như một lời cầu chúc cả gia đình sẽ có một năm mới đầy “Cát Tường”, may mắn,
                        phước lành, mọi việc đều xuôi thuyền thuận gió. Người Việt Nam đều nội hàm, nhất là những ngày
                        quan trọng như Tết Âm Lịch, ai cũng muốn có thật nhiều những lời chúc phúc và may mắn liền kề.
                     </p>
                     <p>Không chỉ riêng việc mình tự mua hoa Cát Tường rồi trưng trong nhà mà tặng bạn bè, đồng nghiệp,
                        người thân những bó hoa, chậu hoa Cát Tường cũng là một lời chúc Tết tốt lành nhất trong dịp Tết
                        Nguyên Đán này đó.</p>
                     <p>Các bó hoa cắm sẵn tại Flowerstore với sự góp mặt của Cát Tường, từ trái sang: Bó hoa cắm sẵn
                        Chiều Xuân và Bó hoa cắm sẵn Giao Mùa.</p>
                     <p>Chúc bạn và những người bạn quý mến sẽ có một cái Tết thật ý nghĩa, thật may mắn và thật hạnh
                        phúc nhé. Happy new year!</p>
